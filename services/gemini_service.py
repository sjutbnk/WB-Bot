import logging
import google.generativeai as genai
from typing import List, Dict, Any, Optional

logger = logging.getLogger(__name__)

class GeminiService:
    """Сервис для интеграции с Gemini API для решения творческих задач (SEO, ТЗ)."""

    def __init__(self, api_key: Optional[str] = None):
        self.api_key = api_key
        self.is_mock = not self.api_key or self.api_key.startswith("your_") or "mock" in self.api_key.lower()
        
        if self.is_mock:
            logger.warning("Gemini API работает в ДЕМО-режиме (заглушки ответов)")
        else:
            genai.configure(api_key=self.api_key)
            # Будем использовать стабильную и быструю модель gemini-1.5-flash
            self.model_name = "gemini-1.5-flash"

    async def generate_seo_description(
        self, 
        title: str, 
        current_description: str, 
        keywords: List[str], 
        characteristics: List[Dict[str, Any]]
    ) -> str:
        """Переписывает описание товара под требования SEO с интеграцией ключевых слов."""
        if self.is_mock:
            return self._get_mock_seo_description(title, keywords)

        prompt = (
            f"Ты — опытный SEO-специалист и копирайтер для маркетплейса Wildberries.\n"
            f"Твоя задача — переписать описание товара так, чтобы оно было продающим, "
            f"структурированным и содержало ключевые поисковые запросы.\n\n"
            f"Название товара: {title}\n"
            f"Текущее описание:\n{current_description}\n\n"
            f"Характеристики:\n"
            f"{self._format_characteristics(characteristics)}\n\n"
            f"Ключевые слова, которые ОБЯЗАТЕЛЬНО нужно гармонично встроить в текст:\n"
            f"{', '.join(keywords)}\n\n"
            f"Требования к тексту:\n"
            f"1. Текст должен быть разбит на логические абзацы (преимущества, для кого подходит, правила ухода/использования).\n"
            f"2. Ключевые слова должны быть вписаны естественно, без спама и грамматических ошибок.\n"
            f"3. Избегай банальных и лишних фраз, сфокусируйся на пользе для покупателя.\n"
            f"4. Объем текста должен быть от 1000 до 2000 символов.\n\n"
            f"Напиши только итоговый вариант описания без лишних комментариев от себя."
        )

        try:
            model = genai.GenerativeModel(self.model_name)
            response = await model.generate_content_async(prompt)
            return response.text.strip()
        except Exception as e:
            logger.error(f"Ошибка вызова Gemini API для SEO: {e}")
            return f"Ошибка генерации SEO. Вот базовый вариант с ключами:\n\n{title}. Отличный выбор для каждого! Ключевые слова: {', '.join(keywords)}"

    async def generate_graphics_brief(
        self, 
        title: str, 
        description: str, 
        characteristics: List[Dict[str, Any]]
    ) -> str:
        """Создает подробное техническое задание (ТЗ) для дизайнера инфографики."""
        if self.is_mock:
            return self._get_mock_graphics_brief(title)

        prompt = (
            f"Ты — эксперт по визуальному маркетингу на маркетплейсах. Твоя задача — спроектировать "
            f"продающую воронку инфографики (структуру слайдов) для карточки товара Wildberries "
            f"и составить подробное ТЗ для дизайнера.\n\n"
            f"Название товара: {title}\n"
            f"Описание товара:\n{description}\n\n"
            f"Характеристики:\n"
            f"{self._format_characteristics(characteristics)}\n\n"
            f"Требования к ТЗ:\n"
            f"1. Спроектируй воронку из 5-6 слайдов:\n"
            f"   - Слайд 1: Главный (кликбейтный, УТП, ключевые выгоды).\n"
            f"   - Слайд 2: Преимущества крупным планом (материал, качество, детали).\n"
            f"   - Слайд 3: Функционал/сферы применения (где и как использовать).\n"
            f"   - Слайд 4: Размерная сетка / Замеры (если применимо) или важные детали посадки.\n"
            f"   - Слайд 5: Уход за изделием / Дополнительная ценность.\n"
            f"   - Слайд 6: Закрывающий (призыв к действию, отзывы, бренд).\n"
            f"2. Для каждого слайда распиши:\n"
            f"   - **Смысловая задача**: что должен понять покупатель за 2 секунды.\n"
            f"   - **Текст на слайде**: лаконичные фразы крупным шрифтом.\n"
            f"   - **Композиция**: что изображено на фото (какой ракурс), какие графические элементы использовать.\n"
            f"   - **Размерная сетка**: если размерная сетка не заполнена в характеристиках, ОБЯЗАТЕЛЬНО сделай акцент на разработке слайда с размерной сеткой и замерами.\n\n"
            f"Ответ отформатируй красивой разметкой Markdown для Telegram (используй списки и жирный шрифт)."
        )

        try:
            model = genai.GenerativeModel(self.model_name)
            response = await model.generate_content_async(prompt)
            return response.text.strip()
        except Exception as e:
            logger.error(f"Ошибка вызова Gemini API для ТЗ инфографики: {e}")
            return "Не удалось сгенерировать ТЗ. Попробуйте повторить запрос позже."

    def _format_characteristics(self, characteristics: List[Dict[str, Any]]) -> str:
        lines = []
        for char in characteristics:
            name = char.get("name", "")
            value = char.get("value", [])
            val_str = ", ".join(value) if isinstance(value, list) else str(value)
            lines.append(f"- {name}: {val_str}")
        return "\n".join(lines)

    # --- Имитации генерации для демо-режима ---

